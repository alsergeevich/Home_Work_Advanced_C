
/*
Поиск последовательности бит
На вход программе подается беззнаковое 32-битное целое число N и натуральное 
число K (1 ≤ K ≤ 31). 
Требуется взять K подряд идущих битов числа N так, чтобы полученное число было максимальным. 
Программа должна вывести полученное число.
*/

#include <stdio.h>
#include <stdint.h>

int main() {
    uint32_t N;
    int K;
    uint32_t max_value = 0;

    // Чтение числа N и K
    scanf("%u %d", &N, &K);

    // Перебор всех возможных позиций для K битов
    for (int i = 0; i <= 32 - K; i++) {
        // Извлечение K битов, начиная с позиции i
        uint32_t current_value = (N >> i) & ((1 << K) - 1);
        if (current_value > max_value) {
            max_value = current_value;
        }
    }

    // Вывод максимального значения
    printf("%u\n", max_value);

    return 0;
}